export function downLoadFile(fileName, data) {
  // 兼容ie11,edge
  if (window.navigator.msSaveOrOpenBlob) {
    try {
      const blobObject = new Blob([data], { type: 'image/jpeg' })
      window.navigator.msSaveOrOpenBlob(blobObject, decodeURI(fileName))
    } catch (e) {
      console.log(e)
    }
  } else {
    console.log(data.data)
    const link = document.createElement('a') // 用a标签进行模拟下载
    const blob = new Blob([data.data])
    link.setAttribute('href', window.URL.createObjectURL(blob))
    link.setAttribute('download', new Date().getTime() + '.png')
    link.style.visibility = 'hidden'
    document.body.appendChild(link)
    link.click()
    document.body.removeChild(link)
  }
}
